#include <stdio.h>
#include <stdlib.h>

int main()
{
    int ascii;
    for(ascii=0;ascii<=127;ascii++)
    {
        printf("%d\t\t",ascii);
    }
    ascii=0;
    puts("=========================================================================================================================================");
    while(ascii<=127)
    {
        printf("%d\t\t",ascii);
        ascii++;
    }
    puts("=========================================================================================================================================");
    ascii=0;
    do
    {
        printf("%d\t\ts",ascii);
        ascii++;
    }while(ascii<=127);
    return 0;
}
